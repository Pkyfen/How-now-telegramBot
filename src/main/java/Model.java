public class Model {
    private String name;
    private double temp;
    private double humiditly;
    private String main;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getTemp() {
        return temp;
    }

    public void setTemp(double temp) {
        this.temp = temp;
    }

    public double getHumiditly() {
        return humiditly;
    }

    public void setHumiditly(double humiditly) {
        this.humiditly = humiditly;
    }


    public String getMain() {
        return main;
    }

    public void setMain(String main) {
        this.main = main;
    }


}
